var jmlAngkot = 10;
var angkotBeroprasi = 8;


for (var noAngkot = 1; noAngkot <= jmlAngkot; noAngkot++) {

    if (noAngkot <= angkotBeroprasi) {
        console.log('Angkot No.' + noAngkot + ' beroprasi dengan baik.')
    } else {
        console.log('Angkot No.' + noAngkot + ' sedang tidak beroprasi')

    }


}